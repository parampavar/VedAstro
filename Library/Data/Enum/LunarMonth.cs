namespace VedAstro.Library
{
    /// <summary>
    /// Names of the Vedic Lunar Months in order
    ///
    /// The Month11 take their names from the fact that on
    /// the full Moon day of each month, a particular constellation
    /// falls and the month is named after it. Thus in
    /// Chitram on the full Moon day we find the constellation Chitta.
    /// </summary>
    public enum LunarMonth
    {
        Empty = 0,
        /// <summary>
        /// Chitta
        /// </summary>
        Chitram = 1,

        /// <summary>
        /// Visakha
        /// </summary>
        Visakham = 2,

        /// <summary>
        /// Jaista
        /// </summary>
        Jaistam = 3,

        /// <summary>
        /// Poorvashadha
        /// </summary>
        Ashadam = 4,

        /// <summary>
        /// Sravana
        /// </summary>
        Sravanam = 5,

        /// <summary>
        /// Poorvabhadra
        /// </summary>
        Bhadrapadam = 6,

        /// <summary>
        /// Aswini
        /// </summary>
        Aswijam = 7,

        /// <summary>
        /// Krithika
        /// </summary>
        Karthikam = 8,

        /// <summary>
        /// Mrigasira
        /// </summary>
        Margasiram = 9,

        /// <summary>
        /// Pooshiami
        /// </summary>
        Pooshiam = 10,

        /// <summary>
        /// Makha
        /// </summary>
        Magham = 11,

        /// <summary>
        /// Phoobba
        /// </summary>
        Phalgunam = 12

    }
}
